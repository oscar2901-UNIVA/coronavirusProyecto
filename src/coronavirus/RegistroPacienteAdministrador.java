 ArchivoEscrituraPaciente AeP= new ArchivoEscrituraPaciente();
       
       
     
        AeP.setNombre(txtAltaNombrePacienteAdmin.getText());
        AeP.setMuniciopio(cmbMunicipio.getSelectedItem().toString());
        AeP.setCorreo(txtAltaCorreoePacienteAdmin.getText());
        AeP.setSexo(cmbSexo.getSelectedItem().toString());
        AeP.setEdad(Integer.parseInt(txtEdadPacienteAdmin.getText()));
        AeP.setNumero(Long.parseLong(txtAltaNumeroPacienteAdmin.getText()));
        
