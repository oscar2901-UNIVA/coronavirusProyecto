    private void btnModificarFarmaciasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarFarmaciasActionPerformed

       List<ArchivoEscrituraFarmacias> listaFarmacias= new ArrayList<>();
        LecturaDeDatos LD = new LecturaDeDatos();
//        
        listaFarmacias =LD.ObtenerFarmacias();
//        
        ArchivoEscrituraFarmacias FarmaciasTemp = new ArchivoEscrituraFarmacias();
       FarmaciasTemp.setNombreFarmacia(txtNombre.getText());
       FarmaciasTemp.setMunicipio(cmbMunicipio.getSelectedItem().toString());
       FarmaciasTemp.setPrecio(cmbPrecio.getSelectedItem().toString());
//        
        FarmaciasTemp.GuardarFarmacia2();
        listaFarmacias.set(index, FarmaciasTemp);
        listaFarmacias.get(index).BorrarFarmacia();
        for (Iterator<ArchivoEscrituraFarmacias> iterator = listaFarmacias.iterator(); iterator.hasNext();) {
            ArchivoEscrituraFarmacias next = iterator.next();
            next.GuardarFarmacia2();
        }
            
       administradorbasededatos.TablaFarmacias();
        
        dispose();
    

    }//GEN-LAST:event_btnModificarFarmaciasActionPerformed

